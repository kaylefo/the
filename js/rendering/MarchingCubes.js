export function marchingCubes(field, nx, ny, nz, origin, dx, isovalue) {
  const positions = [];
  const normals = [];
  const vertCache = new Map();

  function sample(i, j, k) {
    if (i < 0 || j < 0 || k < 0 || i >= nx || j >= ny || k >= nz) return 0;
    return field[i + j * nx + k * nx * ny];
  }

  function vertexInterp(iso, p1, p2, v1, v2) {
    if (Math.abs(iso - v1) < 1e-5) return p1.slice();
    if (Math.abs(iso - v2) < 1e-5) return p2.slice();
    if (Math.abs(v1 - v2) < 1e-5) return p1.slice();
    const t = (iso - v1) / (v2 - v1);
    return [
      p1[0] + t * (p2[0] - p1[0]),
      p1[1] + t * (p2[1] - p1[1]),
      p1[2] + t * (p2[2] - p1[2]),
    ];
  }

  const vertList = new Array(12);

  for (let k = 0; k < nz - 1; k++) {
    for (let j = 0; j < ny - 1; j++) {
      for (let i = 0; i < nx - 1; i++) {
        const vals = [];
        const pts = [];
        for (let c = 0; c < 8; c++) {
          const [ox, oy, oz] = CORNER_OFFSET[c];
          vals[c] = sample(i + ox, j + oy, k + oz);
          pts[c] = [
            origin[0] + (i + ox) * dx,
            origin[1] + (j + oy) * dx,
            origin[2] + (k + oz) * dx,
          ];
        }

        let cubeIndex = 0;
        for (let c = 0; c < 8; c++) {
          if (vals[c] < isovalue) cubeIndex |= 1 << c;
        }
        if (EDGE_FLAGS[cubeIndex] === 0) continue;

        const edges = EDGE_FLAGS[cubeIndex];
        for (let e = 0; e < 12; e++) {
          if (edges & (1 << e)) {
            const [a, b] = EDGE_VERTS[e];
            vertList[e] = vertexInterp(isovalue, pts[a], pts[b], vals[a], vals[b]);
          }
        }

        const tris = TRI_LIST[cubeIndex] || [-1];
        for (let t = 0; t < tris.length && tris[t] !== -1; t += 3) {
          const v0 = vertList[tris[t]];
          const v1 = vertList[tris[t + 1]];
          const v2 = vertList[tris[t + 2]];

          positions.push(v0[0], v0[1], v0[2], v1[0], v1[1], v1[2], v2[0], v2[1], v2[2]);

          const ux = v1[0] - v0[0], uy = v1[1] - v0[1], uz = v1[2] - v0[2];
          const vx = v2[0] - v0[0], vy = v2[1] - v0[1], vz = v2[2] - v0[2];
          const nx_ = uy * vz - uz * vy;
          const ny_ = uz * vx - ux * vz;
          const nz_ = ux * vy - uy * vx;
          const len = Math.hypot(nx_, ny_, nz_) || 1;
          for (let n = 0; n < 3; n++) {
            normals.push(nx_ / len, ny_ / len, nz_ / len);
          }
        }
      }
    }
  }

  return {
    positions: new Float32Array(positions),
    normals: new Float32Array(normals),
    triangleCount: positions.length / 9,
  };
}
